    function showBook(id) {
      const book = books[id];
      document.getElementById("bookList").style.display = "none";
      document.getElementById("bookDetail").style.display = "block";
      document.getElementById("detailTitle").textContent = book.title;
      document.getElementById("detailAuthor").textContent = "by " + book.author;
      document.getElementById("detailDesc").textContent = book.desc;
      document.getElementById("detailImg").src = book.img;
    }

    function goBack() {
      document.getElementById("bookList").style.display = "flex";
      document.getElementById("bookDetail").style.display = "none";
    }
